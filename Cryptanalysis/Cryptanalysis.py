import string


# Alphabete
lower = string.ascii_lowercase
upper = string.ascii_uppercase
letters = string.ascii_letters

print('lower: ', lower, '\n', 'Anzahl Zeichen: ', len(lower))
print('upper: ', upper, '\n', 'Anzahl Zeichen: ', len(upper))
print('letters: ', letters, '\n', 'Anzahl Zeichen: ', len(letters))
